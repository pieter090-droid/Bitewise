--  BOTER (12): roomboter-BAKKERIJ lekte via de boterregel -> juiste familie:
--    2 brood (focaccia knoflookboter, krentenbollen) -> bread_bakery;
--    4 gebak (cupcakes, pastei-bakjes, boterpuntjes, roomboterbollen) ->
--    cakes_pastries; 4 koek (dumkes, krakelingen x2, stroopkoeken) ->
--    cookies_biscuits; kaasvlinders -> cheese_snacks; cacaoboter ->
